    private void EdycjaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EdycjaMouseClicked
        // TODO add your handling code here:
        if (pomocnicza == 0)
        {
            JOptionPane.showMessageDialog(this, "Wybierz użytkownika!");
        }else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ufobd", "root","");
                String Zapytanie = "Update uzytkownikbd set Identyfikator=?, Imie=?, Drugie_imie=?, Nazwisko=?, Numer_Telefonu=?, Adres_email=? where ID=?";
                PreparedStatement Add = Con.prepareStatement(Zapytanie);
                Add.setInt(8, pomocnicza);
                Add.setString(1, IdentyfikatorJAVA.getText());
                Add.setString(2, ImieJAVA.getText());
                Add.setString(3, Imie2JAVA.getText());
                Add.setString(4, NazwiskoJAVA.getText());
                Add.setInt(5, Integer.valueOf(NumerJAVA.getText()));
                Add.setString(6, MailJAVA.getText());
                int wiersz = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Dane zaktualizowane!"); 
                Con.close();
                WyswietlUzytkownikow();
                Czyszczenie();
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_EdycjaMouseClicked

    private void DodajMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DodajMouseClicked
        // TODO add your handling code here:
        if(IdentyfikatorJAVA.getText().isEmpty() || ImieJAVA.getText().isEmpty() || NazwiskoJAVA.getText().isEmpty() || NumerJAVA.getText().isEmpty() || MailJAVA.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Brakuje danych!");
        }else{
            try{
                Licznik();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ufobd", "root","");
                PreparedStatement Add = Con.prepareStatement("insert into uzytkownikbd values(?, ?, ?, ?, ?, ?, ?)");
                Add.setInt(1, UzytkID);
                Add.setString(2, IdentyfikatorJAVA.getText());
                Add.setString(3, ImieJAVA.getText());
                Add.setString(4, Imie2JAVA.getText());
                Add.setString(5, NazwiskoJAVA.getText());
                Add.setString(6, NumerJAVA.getText());
                Add.setString(7, MailJAVA.getText());
                int wiersz = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Użytkownik dodany pomyślnie"); 
                Con.close();
                WyswietlUzytkownikow();
                Czyszczenie();
            } catch(Exception e){
                JOptionPane.showMessageDialog(this,e);
            }
            
        }
    }//GEN-LAST:event_DodajMouseClicked

