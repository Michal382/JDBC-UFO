    private void UsunMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UsunMouseClicked
        // TODO add your handling code here:
        if(pomocnicza==0){
            JOptionPane.showMessageDialog(this, "Wybierz Użytkownika!");
        } else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ufobd", "root","");
                String ZapytanieSQL = "Delete from uzytkownikbd where ID="+pomocnicza;
                Statement Del = Con.createStatement();
                Del.executeUpdate(ZapytanieSQL);
                JOptionPane.showMessageDialog(this, "Użytkownik usunięty!");
                WyswietlUzytkownikow();
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }      
    }//GEN-LAST:event_UsunMouseClicked

