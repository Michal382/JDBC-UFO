    private void WyswietlUzytkownikow()
    {
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ufobd", "root","");
            St = Con.createStatement();
            Rs = St.executeQuery("select * from uzytkownikbd");
            Lista_uzytkownikow.setModel(DbUtils.resultSetToTableModel(Rs));
        } catch (Exception e){
            
        }
    }
    
    private void Czyszczenie()
    {
    IdentyfikatorJAVA.setText("");
    ImieJAVA.setText("");
    Imie2JAVA.setText("");
    NazwiskoJAVA.setText("");
    NumerJAVA.setText("");
    MailJAVA.setText("");  
    }
    
    private void Licznik()
    {
        try{
            St1 = Con.createStatement();
            RS1 = St1.executeQuery("select Max(PID) from uzytkownikbd");
            RS1.next();
            UzytkID = RS1.getInt(1)+1;
        }catch (Exception e){
            
        }
    }
    
    
